"""
Создайте три списка, каждый из которых должен содержать десять значений.
Первый – названия продуктов,
второй – стоимость продукта,
третий – трехзначный код сотрудника, оформившего продажу.
Требуется объединить все три списка в один список.
Рекомендация: используйте встроенную функцию Python zip(), изучите порядок ее работы согласно официальной документации.
"""

products = [
    "Хлеб", "Молоко", "Яйца", "Сыр", "Масло",
    "Колбаса", "Сок", "Чай", "Кофе", "Сахар"
]

prices = [
    45.50, 89.90, 120.00, 250.00, 180.50,
    350.00, 99.90, 150.00, 400.00, 65.00
]

employee_codes = [
    101, 205, 318, 450, 567,
    123, 789, 234, 876, 345
]

combined_list = list(zip(products, prices, employee_codes))

if __name__ == '__main__':
    print("Объединенный список:")
    for item in combined_list:
        print(item)
