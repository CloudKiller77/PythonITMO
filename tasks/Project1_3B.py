"""
Задание 1. Создайте список случайно сгенерированных чисел от 1 до 100.
На основе этого списка создайте второй, который будет содержать слова “High” ("высокий”) или “Low” ("низкий")
в зависимости от того, больше или меньше соответствующее число исходного списка некого порогового значения
(само значение порога должно быть задано вами произвольно как значение переменной).
Задание 2. Создайте список из 100 имен. Для генерации 100 имен можно воспользоваться примером из прошлого модуля
Random_names.py в котором применялся модуль names (его потребуется инсталлировать с помощью pip).
На основе этого списка имен создайте два новых списка. Один с именами, где первый символ начинается
с буквы между “А” и “М”, а другой – содержит остальные имена.
Задание 3. Акростих. Пользователь вводит в консоль слова по одному. Когда пользователь введет пустую строку
(что означает окончание ввода), программа должна вывести слово, составленное из первых букв введенных пользователем слов.
"""

import names
import random

# Задание 1
limit = 50

# Список случайных чисел от 1 до 100
original_list = [random.randint(1, 100) for _ in range(20)]

# Второй список со словами "High" или "Low"
high_low_list = ["High" if num > limit else "Low" for num in original_list]
print("Результат (High/Low):", high_low_list)

# Задание 2
# Генерируем список из 100 имен
names_list = []
for _ in range(100):
    names_list.append(names.get_first_name())

# Список имен с буквами от A до M
list_a_m = [name for name in names_list if 'A' <= name[0].upper() <= 'M']
# Список с остальными именами
list_n_z = [name for name in names_list if name[0].upper() > 'M']

print(f"\nИмена с A по M ({len(list_a_m)} шт.):")
print(list_a_m)
print(f"\nИмена с N по Z ({len(list_n_z)} шт.):")
print(list_n_z)

# Задание 3
# Создание акростих
words = []
word_number = 1

while True:
    word = input(f"Слово {word_number}: ").strip()

    if word == "":
        break

    words.append(word)
    word_number += 1

if words:
    # Составляем слово из первых букв
    acrostic = "".join(word[0] for word in words)
    print(f"Акростих: {acrostic}")
else:
    print("Не было введено ни одного слова.")
